                    It's a guess or rough calculation about time, value, cost, size of something.
                    <br>
                    We are using estimates every day without even knowing. <br>
                    When we want to go out and agree with friends on the time when to meet, we are
                    estimating the time it takes for us to prepare and to get there. The estimation
                    is most of the time precise because we did this many times. <br>
                    When we take a loan from the bank to invest in some courses or certifications,
